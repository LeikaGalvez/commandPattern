package commandPattern;

public class Lights {
    public void turnOn(){
        System.out.println("Turned on the lights!\n");
    }
    public void turnOff(){
        System.out.println("Turned off the lights!\n");
    }
    public void increaseBrightness(){
        System.out.println("Increased brightness by 5.\n");
    }
    public void decreaseBrightness(){
        System.out.println("Decreased brightness by 5.\n");
    }
}
